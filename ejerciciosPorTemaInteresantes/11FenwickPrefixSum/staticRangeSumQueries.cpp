using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    ll n,q; cin>>n>>q;
    vector<ll> v(n,0), sum(n+1,0);
    sum[0] = 0;
    for(ll i = 0; i<n;i++){
        cin>>v[i];
        sum[i+1] = sum[i] + v[i];
    }

    while(q--){
        ll a,b;
        cin>>a>>b;
        cout<<sum[b]-sum[a-1]<<"\n";
    }
  
  return 0;
}

