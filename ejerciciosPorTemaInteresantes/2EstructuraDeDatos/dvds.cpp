#include <bits/stdc++.h>

using namespace std;
signed main (){
  std::ios::sync_with_stdio(false);cin.tie(0);
  int a;
  int n,m;
  while(cin>>n>>m){
    if(!n && !m){
        break;
    }

    set<int>dvds;
    for(int i = 0; i<n;i++){
        int b; cin>>b;
        dvds.insert(b);
    }
    for(int i = 0; i<m;i++){
        int b; cin>>b;
        dvds.insert(b);
    }

    if(dvds.size()==n+m){
        cout<<0<<"\n";
    }else{
        cout<<(n+m)-dvds.size()<<"\n";
    }
  }
  
  return 0;
}

