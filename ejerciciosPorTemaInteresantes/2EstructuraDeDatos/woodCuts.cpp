#include <bits/stdc++.h>

using namespace std;
signed main (){
  std::ios::sync_with_stdio(false);cin.tie(0);
  int n,m;
  cin>>n>>m;
  set<int>cuts;
  cuts.insert(0);
  cuts.insert(n);
  for(int i = 0; i<m; i++){
    int q,x;
    cin>>q>>x;
    if(q == 1){
        cuts.insert(x);
    }else{
        int l = *cuts.upper_bound(x) - *(--cuts.upper_bound(x));
        cout<<l<<"\n";
    }
  }

  
  
  return 0;
}

