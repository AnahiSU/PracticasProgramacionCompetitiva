using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    int c, cont = 0; cin>>c;
    multiset<int> lis;
    while(c--){
        int a; cin>>a;
        if(lis.upper_bound(a) == lis.end()){
            cont++;
        }else{
            lis.erase(lis.upper_bound(a));
        }
        lis.insert(a);
    }
    cout<<cont;
  
  return 0;
}

