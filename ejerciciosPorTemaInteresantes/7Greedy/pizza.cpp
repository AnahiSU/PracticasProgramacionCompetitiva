typedef long long ll;
#define srt(a) sort(a.begin(),a.end());
#define rvs(a) reverse(a.begin(), a.end());
    
#include <bits/stdc++.h>
    
using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    ll n,w; cin>>n>>w;
    vector<pair<ll,ll>> lis;
    while(n--){
        int a,b; cin>>a>>b;
        lis.push_back(make_pair(a,b));
    }
    ll tot = 0;
    srt(lis);
    rvs(lis);
    

    for(int i = 0; i<lis.size();i++){
        if(lis[i].second <= w){     
            w -= lis[i].second;
            tot += (lis[i].first) * (lis[i].second);
        }else{
            tot += w*(lis[i].first);
            break;
        }
    }
    cout<<tot;
  
  return 0;
}

