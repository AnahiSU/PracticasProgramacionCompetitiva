typedef long long ll;
#define srt(a) sort(a.begin(),a.end());
    
#include <bits/stdc++.h>
    
using namespace std;

ll binPow(ll b, ll e, ll mod){
    if(e == 0) return 1;
    if(e == 1) return b;
    ll res = 0;
    if(e%2 == 0){
        res = binPow(b,e/2,mod);
        res = res * res % mod;
    }else{
        res = binPow(b,e-1,mod)*b%mod;
    }
    return res;
}

