typedef long long ll;
#define srt(a) sort(a.begin(),a.end());
    
#include <bits/stdc++.h>
    
using namespace std;
vector<int>dado;
int n; 
ll MOD =  (1e9)+7;

ll dados(int suma){
    if(suma == n) return 1;
    if(dado[suma] != -1) return dado[suma];
    if(suma>n) return 0;

    dado[suma] = 0; // 0 o mas formas
    for(int i = 1; i<7;i++){
        dado[suma] += dados(suma+i);
        dado[suma] %= MOD;
    }
    return dado[suma];
}

signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    cin>>n;
    dado.assign(1000005,-1);
    cout<<dados(0);

  
  return 0;
}

