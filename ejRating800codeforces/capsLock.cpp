typedef long long ll;
#define srt(a) sort(a.begin(),a.end());
    
#include <bits/stdc++.h>
    
using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    string s,res; cin>>s;
    bool flag = false;
    for(int i = 1; i< s.size();i++){
        if(s[i] < 123 && s[i] > 96){
            flag = true;
            break;
        }
    }
    if(!flag){
        for(int i = 0; i<s.size();i++){
            if(s[i] < 123 && s[i] > 96){
                res.push_back(toupper(s[i]));
            }else{
                res.push_back(tolower(s[i]));
            }
        }
    }else{
        res = s;
    }
    
    cout<<res;
  
  return 0;
}

