    int c, cont = 0 ; cin>>c;
    string ant = ""; 
    while(c--){

        string s; cin>>s;
        if(ant == ""){
            ant = s;
            cont++;
        }else{
            
            if(ant != s){
                cont++;
                ant = s;
            }
        }
    }
