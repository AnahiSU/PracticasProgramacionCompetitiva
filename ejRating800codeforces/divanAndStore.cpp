typedef long long ll;
#define srt(a) sort(a.begin(),a.end());
    
#include <bits/stdc++.h>
    
using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    int c; cin>>c;
    while(c--){
        vector<int>choc;
        int a,b,c,d; cin>>a>>b>>c>>d;
        for(int i = 0; i< a; i++){
            int j; cin>>j;
            choc.push_back(j);
        }

        srt(choc);
        int cont = 0, sum = 0;
        for(int i = 0; i<choc.size();i++){
            if(choc[i] <= c && choc[i] >=b){
                if(choc[i] + sum <= d){
                    cont++;
                    sum+=choc[i];
                }
            }
        }
        cout<<cont<<"\n";
    }
  
  return 0;
}

