#include <bits/stdc++.h>
    
using namespace std;
signed main (){
  std::ios::sync_with_stdio(false);cin.tie(0);
  int c; cin>>c;
  while(c--){
    string s; cin>>s;
    int sum1 = 0, sum2 = 0;
    for(int i = 0; i<s.size();i++){
        if(i<3){
            sum1+=s[i]-0;
        }else{
            sum2+=s[i]-0;
        }
    }
    if(sum1 == sum2){
        cout<<"YES"<<"\n";
    }else{
        cout<<"NO"<<"\n";
    }
  }
  
  return 0;
}

