#include <bits/stdc++.h>

using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    int c;cin>>c;
    while(c--){
        int a; cin>>a;
        int ind = -1,cont = 0, ant = 0;
        for(int i = 0; i<a; i++){
            int b;cin>>b;
            if(b == 1){
                if(ant>0 || i==0){
                    ind = i;
                }else{
                    if(ind != -1 && ant == 0){
                        cont+=i-ind-1;
                        ind = i;
                    }else{
                        ind = i;
                    }
                }
            }
            ant = b;
            
        }
        cout<<cont<<"\n";
    }
  
  return 0;
}

