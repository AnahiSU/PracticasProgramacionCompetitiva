#include <bits/stdc++.h>
    
using namespace std;
signed main (){
  std::ios::sync_with_stdio(false);cin.tie(0);
  long long c,m; cin>>c>>m;
  set<long long>lis;
  while(m--){
    long long a; cin>>a;
    lis.insert(a);
  }
  for(auto r : lis){
    if(c%r==0){
        cout<<r<<" "<<c/r<<"\n";
        break;
    }
  }
  
  return 0;
}

