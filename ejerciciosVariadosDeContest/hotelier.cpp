typedef long long ll;
#define srt(a) sort((a).begin(),(a).end());
#define srtR(a) sort((a).rbegin(),(a).rend());
    
#include <bits/stdc++.h>
using namespace std;
    
signed main (){
    ios::sync_with_stdio(false);cin.tie(0);
    int n; cin>>n;
    string s; cin>>s;
    vector<int>v(10,0);
    for(int i = 0; i<n;i++){
        if(s[i] == 'L'){
            for(int j = 0; j<10;j++){
                if(v[j] == 0){
                    v[j] = 1;
                    break;
                }
            }
        }else if(s[i] == 'R'){
            for(int j = 9;j>=0;j--){
                if(v[j] == 0){
                    v[j] = 1;
                    break;
                }
            }
        }else{
            int pos = s[i] - '0';
            v[pos] = 0;
        }
    }
    for(int i : v){
        cout<<i;
    }
    cout<<endl;
  
    return 0;
}

