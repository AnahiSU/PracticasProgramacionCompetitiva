typedef long long ll;
#define srt(a) sort((a).begin(),(a).end());
#define srtR(a) sort((a).rbegin(),(a).rend());
    
#include <bits/stdc++.h>
using namespace std;
    
signed main (){
    ios::sync_with_stdio(false);cin.tie(0);
    int n; cin>>n;
    int q; cin>>q;
    set<int> st;
    st.insert(n);st.insert(0);
    multiset<int> cortes;
    cortes.insert(n);
    while(q--){
        int x; cin>>x;
        st.insert(x);
        int ls = *st.upper_bound(x);
        int li = *(--st.lower_bound(x));

        int resta = abs(li-ls);
        auto exis = cortes.find(resta);
        if(exis != cortes.end()){
            cortes.erase(exis);
        }
        cortes.insert(abs(ls-x));
        cortes.insert(abs(li-x));

        cout<<*(--cortes.end())<<endl;

    }
    
 


    
  
    return 0;
}

