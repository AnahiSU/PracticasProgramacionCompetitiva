signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    int c; cin>>c; 
    while(c--){
      int b; cin>>b;
      vector<int> res;

      while(b>0){
        if(b%2==0){
          res.push_back(0);
        }else{
          if(b % 4 == 1){
            //casos 1001
            res.push_back(1);
            b-=1;
          }else{
            //casos 111
            res.push_back(-1);
            b+=1;
          }
        }
        b/=2;
      }
      cout<<res.size()<<"\n";
      for(int v : res){
        cout<<v<<" ";
      }
      cout<<"\n";

    }
  
  return 0;
}

