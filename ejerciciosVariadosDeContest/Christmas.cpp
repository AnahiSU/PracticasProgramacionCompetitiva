typedef long long ll;
#define srt(a) sort((a).begin(),(a).end());
#define srtR(a) sort((a).rbegin(),(a).rend());
    
#include <bits/stdc++.h>
using namespace std;
const double pi = 3.14159265358979323846;
signed main (){
    ios::sync_with_stdio(false);cin.tie(0);
    cout<<fixed<<setprecision(10);
    double r,l;
    cin>>r>>l;
    double ang = acos(((l*l)-(r*r)-(r*r)) / (-2*r*r));
    ang = (ang*180) / pi;
    double longArc = (ang/360) * 2 * pi*r;
    double peri = 2*pi*r;
    double res = (peri - longArc*2 )/ peri;
    if(isnan(res)) res = 0;
    cout<<res<<endl;
  
    return 0;
}

