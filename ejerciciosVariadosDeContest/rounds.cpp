#include <bits/stdc++.h>
    
using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    int c; cin>>c;
    
    while(c--){
        map<char,int> lis;
        int n,a; cin>>n>>a;
        while(n--){
            char b; cin>>b;
            lis[b]++;
        }
        int cont = 0, res = 0;
        for(int j = 0; j<a;j++){
            for(auto i = lis.begin();i!=lis.end();++i){
                if(i->second != 0){
                    i->second--;
                    cont++;
                }
            }
            res += 7-cont; cont = 0;
        }
        
        cout<<res<<endl;
    }
  
  return 0;
}

