using namespace std;
signed main (){
  std::ios::sync_with_stdio(false);cin.tie(0);
  int c; cin>>c;
  while(c--){
    string res = "";
    for(int i = 0; i<8;i++){
        for(int j = 0; j<8;j++){
            char a;
            cin>>a;
            if(a!='.'){
                res.push_back(a);
            }
        }
    }
    cout<<res<<"\n";
  }
  
  return 0;
}

