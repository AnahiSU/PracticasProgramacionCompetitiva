typedef long long ll;
#define srt(a) sort(a.begin(),a.end());
    
#include <bits/stdc++.h>
    
using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    int x; cin>>x;
    while(x--){
        int a,b,c; cin>>a>>b>>c;
    vector<int> v(3);
    v[0] = a; v[1] = b; v[2] = c;

    srt(v);
    int pasos = 5;
    for(int i = 0; i<pasos;i++){
        if(v[0] <= v[1] & v[0] <= v[2]){
            v[0]++;
        }else{
            if(v[1] <= v[0] && v[1] <= v[2]){
                v[1]++;
            }else{
                v[2]++;
            }
        }
        srt(v);
    }
    
    cout<<v[0]*v[1]*v[2]<<"\n";
    }
    
  
  return 0;
}

