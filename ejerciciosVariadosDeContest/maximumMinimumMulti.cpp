using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    int c; cin>>c;
    while(c--){
        int a, n = 2, may = -1, ind = -1, sum = 0;
        cin>>a;
        while(n<=a){
            int i = 1;
            while(i*n <= a){
                sum+=i*n;
                i++;
            }
            if(sum > may){
                
                ind = n;
                may = sum;
                
            }
            sum = 0;
            n++;
        }
        cout<<ind<<endl;
    }
  
  return 0;
}

