typedef long long ll;
#define srt(a) sort(a.begin(),a.end());
    
#include <bits/stdc++.h>
    
using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    string s1; cin>>s1;
    transform(s1.begin(), s1.end(), s1.begin(), ::tolower);
    string s2 = s1;
    srt(s2);
    if(s1 == s2){
        cout<<"yes"<<"\n";
    }else{
        reverse(s2.begin(),s2.end());
        if(s2 == s1){
            cout<<"yes"<<"\n";
        }else{
            cout<<"no"<<"\n";
        }
    }
  
  return 0;
}

