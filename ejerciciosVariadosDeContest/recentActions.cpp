#define srt(a) sort((a).begin(),(a).end());
#define srtR(a) sort((a).rbegin(),(a).rend());
    
#include <bits/stdc++.h>
using namespace std;
    
signed main (){
    ios::sync_with_stdio(false);cin.tie(0);
    int c; cin>>c;
    while(c--){
        int a,b;
        cin>>a>>b;
        vector<int> v(100001,-1),t(100001,-1);
        vector<int> nums;
        
        for(int i = 0; i<a;i++){
            nums.push_back(i+1);
            v[i+1] = 1;
        }

        for(int i = 0; i<b;i++){
          int q;cin>>q;
          if(v[q] == -1 && !nums.empty()){
            int pos = nums.back();
            t[pos] = i+1;
            nums.pop_back();
            v[q] = 1;
          }
        };

        for(int i = 1;i<=a;i++){
          cout<<t[i]<<" ";
        }
        cout<<endl;
    }
  
    return 0;
}

