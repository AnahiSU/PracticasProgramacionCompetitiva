signed main (){
    ios::sync_with_stdio(false);cin.tie(0);

    string s;cin>>s;
    stack<char> t;

    int less = 0;
    string res = "";
    multiset<char>crj;
    for(int i = 0; i<s.size();i++){
        crj.insert(s[i]);
    }
    
    char min;
    for(int i = 0; i<s.size();i++){
        t.push(s[i]);
        auto it = crj.find(s[i]);
        crj.erase(it);
        min = *crj.begin();
        while(!t.empty() && t.top() <= min){
            res.push_back(t.top());
            t.pop();
        }
       
    }
    while(!t.empty()){
        res.push_back(t.top());
        t.pop();
    }
    cout<<res;
    cout<<endl;
    
   
    return 0;
}

