typedef long long ll;
#define srt(a) sort(a.begin(),a.end());
    
#include <bits/stdc++.h>
    
using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    int n,w; cin>>n>>w;
    vector<int> v(n);
    map<int,int> mapita;
    for(int i = 0; i<n; i++){
        cin>>v[i];
        mapita[v[i]]++;
    }
    srt(v);
    bool flag = 1;
    for(auto &a : mapita){
        if(a.second>w){
            flag = 0;
            break;
        }
    }
    (flag) ? cout<<"yes"<<"\n" : cout<<"no"<<"\n";
    
  return 0;
}

