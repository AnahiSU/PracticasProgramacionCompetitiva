#include <bits/stdc++.h>

using namespace std;
signed main (){
  std::ios::sync_with_stdio(false);cin.tie(0);
  int c; cin>>c;
  while(c--){
    int b;cin>>b;
    int max = -10,ind = -1;
    for(int i = 0; i<b;i++){
        int a,k;cin>>a>>k;
        if(a<=10){
            if(max<k){
                max = k;
                ind = i;
            }
        }
    }
    cout<<ind+1<<"\n";
  }
  
  
  return 0;
}

