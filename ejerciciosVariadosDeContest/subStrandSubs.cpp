using namespace std;
signed main (){
    std::ios::sync_with_stdio(false);cin.tie(0);
    int c; cin>>c;
    while(c--){
        string s2,s1;
        int cont = 0,ind = -1,ant = -1;
        cin>>s1>>s2;
        for(int i = 0; i<s2.size();i++){
            for(int j = 0; j<s1.size();j++){
                if(s2[i] != s1[j]){
                    if(j==s1.size()-1){
                        cont++;
                    }
                }else{
                    ind = j;
                    break;
                }
            }
            if(ind != -1 && ant != -1){
                if(ind < ant){
                    cont++;   
                }
                
            }
            ant = ind;
        }
        
        cont += s1.size();
        
        cout<<cont<<"\n";
    }
  
  return 0;
}

