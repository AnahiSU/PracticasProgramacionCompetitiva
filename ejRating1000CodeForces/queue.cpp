#include <bits/stdc++.h>
    
using namespace std;
signed main (){
  std::ios::sync_with_stdio(false);cin.tie(0);
  int c;cin>>c;
  vector<int>per;
  while(c--){
    int a; cin>>a;
    per.push_back(a);
  } 
  sort(per.begin(), per.end());
  int sum = 0,con = 0;
  for(int i = 0; i<per.size();i++){
    if(sum <= per[i]){
        con++;
        sum += per[i];
    }
    
  }
  cout<<con;
  return 0;
}

